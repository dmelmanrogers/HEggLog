makeValueFunction :: Text -> [(LLVMType, Register)] -> [(Int, LLVMType, LLVMOperand)] -> LLVMFunction
makeValueFunction name params stores =
  LLVMFunction
    { functionName = name
    , functionReturnType = LPtr
    , functionParams = params
    , functionBlocks =
        [ LLVMBlock
            "entry"
            ( ICall (Just (Register "obj")) LPtr (DirectCall allocObjectFunctionName) False []
                : concatMap storeField stores
            )
            (TRet LPtr (OLocal LPtr (Register "obj")))
        ]
    }
 where
  storeField (index, ty, value) =
    [ IGetElementPtr (Register ("field" <> Text.pack (show index))) objectType (OLocal LPtr (Register "obj")) (objectField index)
    , IStore ty value (OLocal LPtr (Register ("field" <> Text.pack (show index))))
    ]

forceFunction :: LLVMFunction
forceFunction =
  LLVMFunction
    { functionName = forceFunctionName
    , functionReturnType = LPtr
    , functionParams = [(LPtr, Register "obj")]
    , functionBlocks =
        [ LLVMBlock
            "entry"
            [IIcmp (Register "is_null") ICmpEq LPtr (OLocal LPtr (Register "obj")) OConstNull]
            (TCondBr (OLocal LI1 (Register "is_null")) "abort" "check")
        , LLVMBlock
            "check"
            [ IGetElementPtr (Register "tag_ptr") objectType (OLocal LPtr (Register "obj")) objectField0
            , ILoad (Register "tag") LI64 (OLocal LPtr (Register "tag_ptr"))
            , IIcmp (Register "is_thunk") ICmpEq LI64 (OLocal LI64 (Register "tag")) (OConstInt LI64 tagThunk)
            ]
            (TCondBr (OLocal LI1 (Register "is_thunk")) "enter_thunk" "not_thunk")
        , LLVMBlock
            "not_thunk"
            [IIcmp (Register "is_indirection") ICmpEq LI64 (OLocal LI64 (Register "tag")) (OConstInt LI64 tagIndirection)]
            (TCondBr (OLocal LI1 (Register "is_indirection")) "indirection" "not_indirection")
        , LLVMBlock
            "indirection"
            [ IGetElementPtr (Register "value_ptr") objectType (OLocal LPtr (Register "obj")) objectField4
            , ILoad (Register "value") LPtr (OLocal LPtr (Register "value_ptr"))
            ]
            (TRet LPtr (OLocal LPtr (Register "value")))
        , LLVMBlock
            "not_indirection"
            [IIcmp (Register "is_blackhole") ICmpEq LI64 (OLocal LI64 (Register "tag")) (OConstInt LI64 tagBlackHole)]
            (TCondBr (OLocal LI1 (Register "is_blackhole")) "abort" "return_value")
        , LLVMBlock
            "return_value"
            []
            (TRet LPtr (OLocal LPtr (Register "obj")))
        , LLVMBlock
            "enter_thunk"
            [ IStore LI64 (OConstInt LI64 tagBlackHole) (OLocal LPtr (Register "tag_ptr"))
            , IGetElementPtr (Register "code_ptr") objectType (OLocal LPtr (Register "obj")) objectField2
            , ILoad (Register "code") LPtr (OLocal LPtr (Register "code_ptr"))
            , IGetElementPtr (Register "env_ptr") objectType (OLocal LPtr (Register "obj")) objectField3
            , ILoad (Register "env") LPtr (OLocal LPtr (Register "env_ptr"))
            , IGetElementPtr (Register "update_ptr") objectType (OLocal LPtr (Register "obj")) objectField5
            , ILoad (Register "update") LI64 (OLocal LPtr (Register "update_ptr"))
            , ICall (Just (Register "result")) LPtr (IndirectCall (OLocal LPtr (Register "code"))) False [(LPtr, OLocal LPtr (Register "env"))]
            , IIcmp (Register "is_single") ICmpEq LI64 (OLocal LI64 (Register "update")) (OConstInt LI64 singleEntryCode)
            ]
            (TCondBr (OLocal LI1 (Register "is_single")) "single_entry" "update_thunk")
        , LLVMBlock
            "single_entry"
            [IStore LI64 (OConstInt LI64 tagThunk) (OLocal LPtr (Register "tag_ptr"))]
            (TRet LPtr (OLocal LPtr (Register "result")))
        , LLVMBlock
            "update_thunk"
            [ IGetElementPtr (Register "result_ptr") objectType (OLocal LPtr (Register "obj")) objectField4
            , IStore LPtr (OLocal LPtr (Register "result")) (OLocal LPtr (Register "result_ptr"))
            , IStore LI64 (OConstInt LI64 tagIndirection) (OLocal LPtr (Register "tag_ptr"))
            ]
            (TRet LPtr (OLocal LPtr (Register "result")))
        , LLVMBlock
            "abort"
            [ICall Nothing LVoid (DirectCall "abort") False []]
            TUnreachable
        ]
    }

