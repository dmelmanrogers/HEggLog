uniqueTexts :: [Text] -> [Text]
uniqueTexts =
  reverse . snd . foldl step (Set.empty, [])
 where
  step (seen, acc) value
    | value `Set.member` seen = (seen, acc)
    | otherwise = (Set.insert value seen, value : acc)

