data STGLLVMError
  = STGLLVMInvalidSTG [STGValidate.STGValidationError]
  | STGLLVMUnknownMain Text
  | STGLLVMAmbiguousMain Text [RName]
  | STGLLVMUnsupported Text
  | STGLLVMInvalidLLVM LLVMValidationError
  deriving stock (Show, Eq)

data ValueRef
  = DirectValue LLVMOperand
  | CapturedValue LLVMOperand Int Int
  deriving stock (Show, Eq, Ord)

type ValueEnv = Map.Map RName ValueRef

data PendingClosure = PendingClosure
  { pendingEnvPointer :: LLVMOperand
  , pendingCaptures :: [RName]
  }
  deriving stock (Show, Eq, Ord)

data AllocatedBinding = AllocatedBinding
  { allocatedName :: RName
  , allocatedObject :: LLVMOperand
  , allocatedPending :: Maybe PendingClosure
  }
  deriving stock (Show, Eq, Ord)

data ForeignIntegerSignedness
  = ForeignSigned
  | ForeignUnsigned
  deriving stock (Show, Eq, Ord)

data ForeignABIType
  = ForeignABIInteger LLVMType ForeignIntegerSignedness
  | ForeignABIFloating LLVMType
  | ForeignABIBool
  | ForeignABIChar
  | ForeignABIPointer
  | ForeignABIVoid
  deriving stock (Show, Eq, Ord)

data ForeignFunctionABI = ForeignFunctionABI
  { foreignFunctionSymbol :: Text
  , foreignFunctionArguments :: [ForeignABIType]
  , foreignFunctionResult :: ForeignABIType
  }
  deriving stock (Show, Eq, Ord)

data ModuleState = ModuleState
  { nextClosureFunction :: Int
  , generatedFunctions :: [LLVMFunction]
  , generatedForeignWrappers :: Map.Map RName [Text]
  , moduleWrapperImports :: [CoreForeignImport]
  }
  deriving stock (Show, Eq)

data FunctionState = FunctionState
  { nextRegister :: Int
  , nextBlock :: Int
  , completedBlocks :: [LLVMBlock]
  , currentLabel :: Text
  , currentInstructions :: [LLVMInstruction]
  }
  deriving stock (Show, Eq)

type ModuleM = StateT ModuleState (Either STGLLVMError)
type FunctionM = StateT FunctionState ModuleM

lowerSTGProgramToLLVM :: Text -> STGProgram -> Either STGLLVMError LLVMModule
lowerSTGProgramToLLVM mainOccurrence program =
  case STGValidate.validateProgram program of
    Left errors -> Left (STGLLVMInvalidSTG errors)
    Right () -> do
      mainBinder <- findMain mainOccurrence program
      lowerValidatedSTGProgram mainBinder program

lowerSTGProgramToLLVMByName :: RName -> STGProgram -> Either STGLLVMError LLVMModule
lowerSTGProgramToLLVMByName mainName program =
  case STGValidate.validateProgram program of
    Left errors -> Left (STGLLVMInvalidSTG errors)
    Right () ->
      case [binder | bind <- stgProgramBinds program, binder <- stgBindersOf bind, stgBinderName binder == mainName] of
        [] -> Left (STGLLVMUnknownMain (nameOcc mainName))
        [mainBinder] -> lowerValidatedSTGProgram mainBinder program
        binders -> Left (STGLLVMAmbiguousMain (nameOcc mainName) (map stgBinderName binders))

lowerValidatedSTGProgram :: STGBinder -> STGProgram -> Either STGLLVMError LLVMModule
lowerValidatedSTGProgram mainBinder program = do
  foreignDeclarations <- foreignImportDeclarations program
  (mainFunction, mainModuleState) <- runStateT (lowerMainFunction program mainBinder) (initialModuleState program)
  (foreignExportFunctions, moduleState) <-
    runStateT
      (traverse (lowerForeignExportFunction program) (stgProgramForeignExports program))
      mainModuleState
  let llvmModule =
        LLVMModule
          { moduleComments =
              [ "generated by HeggLog Haskell 2010 STG LLVM backend v0"
              , "values are boxed; thunks are forced through a lazy enter/apply runtime"
              , "heap objects use process-lifetime allocation; generated programs do not free or collect them"
              ]
                <> renderForeignLinkMetadataComments
                  (foreignLinkMetadataForImportsExports (foreignImportsInProgram program) (stgProgramForeignExports program))
          , moduleGlobals = formatGlobals
          , moduleDeclarations = uniqueTexts (runtimeDeclarations <> foreignDeclarations)
          , moduleFunctions = runtimeFunctions <> generatedFunctions moduleState <> foreignExportFunctions <> [mainFunction]
          }
  mapLeft STGLLVMInvalidLLVM (validateLLVMModule llvmModule)
  pure llvmModule

initialModuleState :: STGProgram -> ModuleState
initialModuleState program =
  ModuleState
    { nextClosureFunction = 0
    , generatedFunctions = []
    , generatedForeignWrappers = Map.empty
    , moduleWrapperImports = wrapperImportsInProgram program
    }

findMain :: Text -> STGProgram -> Either STGLLVMError STGBinder
findMain occurrence (STGProgram _ binds _foreignExports) =
  case [binder | bind <- binds, binder <- stgBindersOf bind, nameOcc (stgBinderName binder) == occurrence] of
    [] -> Left (STGLLVMUnknownMain occurrence)
    [binder] -> Right binder
    binders -> Left (STGLLVMAmbiguousMain occurrence (map stgBinderName binders))

lowerMainFunction :: STGProgram -> STGBinder -> ModuleM LLVMFunction
lowerMainFunction (STGProgram _ binds _foreignExports) mainBinder =
  runFunction "main" LI32 [] $ do
    env <- emitRecursiveGroup Map.empty (concatMap bindPairs binds)
    mainObject <- resolveName env (stgBinderName mainBinder)
    if stgBinderType mainBinder == ioTy unitTy
      then do
        ioResult <- emitForce mainObject
        emitIOResultExitCode ioResult
      else do
        printedValue <- printableMainValue (stgBinderType mainBinder) mainObject
        fmt <- formatPointer (stgBinderType mainBinder)
        printedReg <- freshRegister "printed"
        emit (ICall (Just printedReg) LI32 (DirectCall "printf") True [(LPtr, fmt), (operandType printedValue, printedValue)])
        pure (OConstInt LI32 0)

lowerForeignExportFunction :: STGProgram -> CoreForeignExport -> ModuleM LLVMFunction
lowerForeignExportFunction program foreignExport = do
  abi <- liftEitherModule (foreignExportFunctionABI foreignExport)
  let (_argumentTypes, declaredResultTy) = splitForeignFunctionType (coreForeignExportType foreignExport)
      params = foreignABIParameters abi
      symbol = foreignFunctionSymbol abi
  case foreignFunctionResult abi of
    ForeignABIVoid ->
      runVoidFunction symbol params $ do
        resultObject <- emitForeignExportResult program foreignExport abi params
        _ <- emitForeignReturnValue declaredResultTy resultObject
        pure ()
    resultABI ->
      runFunction symbol (foreignABITypeLLVM resultABI) params $ do
        resultObject <- emitForeignExportResult program foreignExport abi params
        forcedResult <- emitForeignReturnValue declaredResultTy resultObject
        emitUnboxForeignReturn resultABI forcedResult

emitForeignExportResult :: STGProgram -> CoreForeignExport -> ForeignFunctionABI -> [(LLVMType, Register)] -> FunctionM LLVMOperand
emitForeignExportResult program foreignExport abi params = do
  env <- emitRecursiveGroup Map.empty (concatMap bindPairs (stgProgramBinds program))
  exportedObject <- resolveName env (coreForeignExportName foreignExport)
  boxedArguments <-
    traverse
      ( \(argumentABI, (argumentTy, argumentRegister)) ->
          emitBoxForeignResult argumentABI (OLocal argumentTy argumentRegister)
      )
      (zip (foreignFunctionArguments abi) params)
  applyHaskellFunction exportedObject boxedArguments

printableMainValue :: CoreType -> LLVMOperand -> FunctionM LLVMOperand
printableMainValue ty value
  | ty == intTy = do
      reg <- freshRegister "main_int"
      emit (ICall (Just reg) LI64 (DirectCall expectIntFunctionName) False [(LPtr, value)])
      pure (OLocal LI64 reg)
  | ty == boolTy = do
      boolReg <- freshRegister "main_bool"
      emit (ICall (Just boolReg) LI1 (DirectCall expectBoolFunctionName) False [(LPtr, value)])
      intReg <- freshRegister "main_bool_i32"
      emit (IZext intReg (OLocal LI1 boolReg) LI32)
      pure (OLocal LI32 intReg)
  | ty == charTy = do
      charReg <- freshRegister "main_char"
      emit (ICall (Just charReg) LI32 (DirectCall expectCharFunctionName) False [(LPtr, value)])
      pure (OLocal LI32 charReg)
  | otherwise =
      throwSTGLLVM (STGLLVMUnsupported ("native Haskell 2010 main must be Int, Bool, or Char, got " <> renderCoreType ty))

formatPointer :: CoreType -> FunctionM LLVMOperand
formatPointer ty = do
  reg <- freshRegister "fmt"
  let formatSpec
        | ty == boolTy = ("haskell2010_fmt_i1", boolFormatBytes)
        | ty == charTy = ("haskell2010_fmt_char", charFormatBytes)
        | otherwise = ("haskell2010_fmt_i64", intFormatBytes)
      (globalName, bytes) = formatSpec
      arrayType = LArray (Text.length bytes) LI8
  emit
    ( IGetElementPtr
        reg
        arrayType
        (OGlobal LPtr globalName)
        [(LI64, OConstInt LI64 0), (LI64, OConstInt LI64 0)]
    )
  pure (OLocal LPtr reg)

emitExpr :: ValueEnv -> STGExpr -> FunctionM LLVMOperand
emitExpr env = \case
  STGAtom atom ->
    emitAtomValue env atom
  STGApp callee arguments _ -> do
    calleeObject <- resolveName env callee
    functionObject <- emitExpectFunction calleeObject
    case arguments of
      [argument] -> do
        argumentObject <- emitAtomAddress env argument
        enterFunction functionObject argumentObject
      _ ->
        throwSTGLLVM
          ( STGLLVMUnsupported
              ("native STG enter/apply currently expects unary curried calls to `" <> renderRName callee <> "`")
          )
  STGLet bind body _ -> do
    env' <- emitBind env bind
    emitExpr env' body
  STGCase scrutinee binder alternatives _ -> do
    scrutineeObject <- emitExpr env scrutinee >>= emitForce
    emitCase env binder alternatives scrutineeObject
  STGPrim op arguments _ ->
    emitPrim env op arguments
  STGForeignCall foreignImport arguments _ ->
    emitForeignCall env foreignImport arguments
  STGForeignImportValue foreignImport _ ->
    emitForeignImportValue foreignImport

emitBind :: ValueEnv -> STGBind -> FunctionM ValueEnv
emitBind env = \case
  STGNonRec binder rhs -> do
    allocated <- emitRhsObject env binder rhs
    let env' = Map.insert (stgBinderName binder) (DirectValue (allocatedObject allocated)) env
    fillPending env' allocated
    pure env'
  STGRec pairs ->
    emitRecursiveGroup env pairs

emitRecursiveGroup :: ValueEnv -> [(STGBinder, STGRhs)] -> FunctionM ValueEnv
emitRecursiveGroup env pairs = do
  let recCaptureEnv =
        Map.union
          (Map.fromList [(stgBinderName binder, DirectValue OConstNull) | (binder, _) <- pairs])
          env
  allocated <- traverse (uncurry (emitRhsObject recCaptureEnv)) pairs
  let recEnv =
        foldr
          (\binding -> Map.insert (allocatedName binding) (DirectValue (allocatedObject binding)))
          env
          allocated
  mapM_ (fillPending recEnv) allocated
  pure recEnv

emitRhsObject :: ValueEnv -> STGBinder -> STGRhs -> FunctionM AllocatedBinding
emitRhsObject env binder rhs =
  case rhs of
    STGFunction [argument] body -> do
      let captures = capturedNames env (freeVarsRhs rhs)
      entry <- defineFunctionEntry "fun" (stgBinderName binder) captures argument body
      envPointer <- emitAllocEnvSlots (length captures)
      object <- emitMakeFunction entry envPointer
      pure (AllocatedBinding (stgBinderName binder) object (Just (PendingClosure envPointer captures)))
    STGFunction arguments _ ->
      throwSTGLLVM
        ( STGLLVMUnsupported
            ( "native STG lowering expects unary curried functions after Core-to-STG lowering; `"
                <> renderRName (stgBinderName binder)
                <> "` has arity "
                <> Text.pack (show (length arguments))
            )
        )
    STGThunk updateFlag body -> do
      let captures = capturedNames env (freeVarsRhs rhs)
      entry <- defineThunkEntry "thunk" (stgBinderName binder) captures body
      envPointer <- emitAllocEnvSlots (length captures)
      object <- emitMakeThunk entry envPointer updateFlag
      pure (AllocatedBinding (stgBinderName binder) object (Just (PendingClosure envPointer captures)))
    STGConstructor name fields resultTy ->
      do
        fieldObjects <- traverse (emitAtomAddress env) fields
        object <- emitConstructorObject name resultTy fieldObjects
        pure (AllocatedBinding (stgBinderName binder) object Nothing)

fillPending :: ValueEnv -> AllocatedBinding -> FunctionM ()
fillPending env allocated =
  case allocatedPending allocated of
    Nothing -> pure ()
    Just pending ->
      forM_ (zip [0 :: Int ..] (pendingCaptures pending)) $ \(index, name) -> do
        value <- resolveName env name
        emitStoreEnvSlot (length (pendingCaptures pending)) (pendingEnvPointer pending) index value

defineFunctionEntry :: Text -> RName -> [RName] -> STGBinder -> STGExpr -> FunctionM Text
defineFunctionEntry prefix name captures argument body =
  lift $ do
    functionName <- freshClosureFunctionName prefix name
    function <-
      runFunction
        functionName
        LPtr
        [(LPtr, Register "env"), (LPtr, Register "arg")]
        $ do
          let envOperand = OLocal LPtr (Register "env")
              argumentOperand = OLocal LPtr (Register "arg")
              capturedEnv = captureEnv envOperand captures
              bodyEnv =
                Map.insert (stgBinderName argument) (DirectValue argumentOperand) capturedEnv
          emitExpr bodyEnv body
    addGeneratedFunction function
    pure functionName

defineThunkEntry :: Text -> RName -> [RName] -> STGExpr -> FunctionM Text
defineThunkEntry prefix name captures body =
  lift $ do
    functionName <- freshClosureFunctionName prefix name
    function <-
      runFunction functionName LPtr [(LPtr, Register "env")] $ do
        let envOperand = OLocal LPtr (Register "env")
        emitExpr (captureEnv envOperand captures) body
    addGeneratedFunction function
    pure functionName

captureEnv :: LLVMOperand -> [RName] -> ValueEnv
captureEnv envOperand captures =
  Map.fromList
    [ (name, CapturedValue envOperand index (length captures))
    | (index, name) <- zip [0 ..] captures
    ]

capturedNames :: ValueEnv -> Set.Set RName -> [RName]
capturedNames env names =
  [name | name <- Map.keys env, name `Set.member` names]

emitAtomValue :: ValueEnv -> STGAtom -> FunctionM LLVMOperand
emitAtomValue env atom =
  emitAtomAddress env atom >>= emitForce

emitAtomAddress :: ValueEnv -> STGAtom -> FunctionM LLVMOperand
emitAtomAddress env = \case
  STGVar name _ ->
    resolveName env name
  STGLit literal _ ->
    emitLiteral literal
  STGCon name ty ->
    emitConstructorObject name ty []

emitLiteral :: Literal -> FunctionM LLVMOperand
emitLiteral = \case
  LInt value ->
    case mkHIntLiteral value of
      Right intValue -> emitMakeInt (hintToInteger intValue)
      Left err -> throwSTGLLVM (STGLLVMUnsupported ("native Int literal is out of range: " <> renderIntError err))
  LChar value ->
    emitMakeChar (fromIntegral (ord value))
  LString value ->
    emitMakeStringLiteral value

emitConstructorObject :: RName -> CoreType -> [LLVMOperand] -> FunctionM LLVMOperand
emitConstructorObject name ty fields
  | ty == boolTy && name == trueDataConName = emitMakeBool (OConstInt LI1 1)
  | ty == boolTy && name == falseDataConName = emitMakeBool (OConstInt LI1 0)
  | otherwise =
      emitMakeData name fields

emitPrim :: ValueEnv -> CorePrimOp -> [STGAtom] -> FunctionM LLVMOperand
emitPrim env op arguments =
  case (op, arguments) of
    (PrimAdd, [lhs, rhs]) -> emitIntBinary env "llvm.sadd.with.overflow.i64" lhs rhs >>= emitMakeIntOperand
    (PrimSub, [lhs, rhs]) -> emitIntBinary env "llvm.ssub.with.overflow.i64" lhs rhs >>= emitMakeIntOperand
    (PrimMul, [lhs, rhs]) -> emitIntBinary env "llvm.smul.with.overflow.i64" lhs rhs >>= emitMakeIntOperand
    (PrimDiv, [lhs, rhs]) -> emitCheckedDiv env lhs rhs >>= emitMakeIntOperand
    (PrimRem, [lhs, rhs]) -> emitCheckedRem env lhs rhs >>= emitMakeIntOperand
    (PrimLt, [lhs, rhs]) -> do
      lhsValue <- emitExpectAtomInt env lhs
      rhsValue <- emitExpectAtomInt env rhs
      reg <- freshRegister "lt"
      emit (IIcmp reg ICmpSlt LI64 lhsValue rhsValue)
      emitMakeBool (OLocal LI1 reg)
    (PrimEq, [lhs, rhs])
      | stgAtomType lhs == intTy -> do
          lhsValue <- emitExpectAtomInt env lhs
          rhsValue <- emitExpectAtomInt env rhs
          reg <- freshRegister "eq"
          emit (IIcmp reg ICmpEq LI64 lhsValue rhsValue)
          emitMakeBool (OLocal LI1 reg)
      | stgAtomType lhs == boolTy -> do
          lhsValue <- emitExpectAtomBool env lhs
          rhsValue <- emitExpectAtomBool env rhs
          reg <- freshRegister "eq"
          emit (IIcmp reg ICmpEq LI1 lhsValue rhsValue)
          emitMakeBool (OLocal LI1 reg)
      | stgAtomType lhs == charTy -> do
          lhsValue <- emitExpectAtomChar env lhs
          rhsValue <- emitExpectAtomChar env rhs
          reg <- freshRegister "eq"
          emit (IIcmp reg ICmpEq LI32 lhsValue rhsValue)
          emitMakeBool (OLocal LI1 reg)
      | otherwise ->
          throwSTGLLVM
            ( STGLLVMUnsupported
                ("native equality is implemented for Int, Bool, and Char, got " <> renderCoreType (stgAtomType lhs))
            )
    (PrimNegate, [value]) -> do
      valueOperand <- emitExpectAtomInt env value
      emitCheckedSub (OConstInt LI64 0) valueOperand >>= emitMakeIntOperand
    (PrimCharToInt, [value]) -> do
      valueOperand <- emitExpectAtomChar env value
      extendedReg <- freshRegister "char_i64"
      emit (IZext extendedReg valueOperand LI64)
      emitMakeIntOperand (OLocal LI64 extendedReg)
    (PrimIntToChar, [value]) -> do
      valueOperand <- emitExpectAtomInt env value
      emitCheckCharCode valueOperand
      emitMakeCharOperand valueOperand
    (PrimShowInt, [value]) -> do
      valueOperand <- emitExpectAtomInt env value
      resultReg <- freshRegister "show_int"
      emit (ICall (Just resultReg) LPtr (DirectCall showIntFunctionName) False [(LI64, valueOperand)])
      pure (OLocal LPtr resultReg)
    (PrimShowBool, [value]) -> do
      valueOperand <- emitExpectAtomBool env value
      emitShowBool valueOperand
    (PrimPutStrLn, [value]) -> do
      valueObject <- emitAtomAddress env value
      emit (ICall Nothing LVoid (DirectCall putStrLnFunctionName) False [(LPtr, valueObject)])
      unitObject <- emitConstructorObject unitDataConName unitTy []
      emitMakeIOSuccess unitObject
    (PrimGetLine, []) -> do
      resultReg <- freshRegister "getline"
      emit (ICall (Just resultReg) LPtr (DirectCall getLineFunctionName) False [])
      emitMakeIOSuccess (OLocal LPtr resultReg)
    (PrimIOThen, [firstAction, secondAction]) -> do
      firstObject <- emitAtomAddress env firstAction
      firstResult <- emitForce firstObject
      emitIOResultBranch
        firstResult
        (pure firstResult)
        (emitAtomAddress env secondAction >>= emitForce)
    (PrimIOBind, [firstAction, continuation]) -> do
      firstObject <- emitAtomAddress env firstAction
      firstResult <- emitForce firstObject
      emitIOResultBranch firstResult (pure firstResult) $ do
        firstPayload <- emitIOResultPayload firstResult
        continuationObject <- emitAtomAddress env continuation
        functionObject <- emitExpectFunction continuationObject
        secondAction <- enterFunction functionObject firstPayload
        emitForce secondAction
    (PrimIOReturn, [value]) ->
      emitAtomAddress env value >>= emitMakeIOSuccess
    (PrimIOFail, [message]) -> do
      messageObject <- emitAtomAddress env message
      errorObject <- emitUserIOError messageObject
      emitMakeIOFailure errorObject
    (PrimIOError, [value]) ->
      emitAtomAddress env value >>= emitMakeIOFailure
    (PrimNullPtr, []) ->
      emitMakePointer OConstNull
    (PrimCastPtr, [pointer]) ->
      emitAtomValue env pointer
    (PrimIsNullPtr, [pointer]) -> do
      pointerValue <- emitExpectAtomPointer env pointer
      reg <- freshRegister "is_null_ptr"
      emit (IIcmp reg ICmpEq LPtr pointerValue OConstNull)
      emitMakeBool (OLocal LI1 reg)
    (PrimIOCatch, [action, handler]) -> do
      actionObject <- emitAtomAddress env action
      actionResult <- emitForce actionObject
      emitIOResultBranch actionResult
        ( do
            errorObject <- emitIOResultPayload actionResult
            handlerObject <- emitAtomAddress env handler
            functionObject <- emitExpectFunction handlerObject
            handledAction <- enterFunction functionObject errorObject
            emitForce handledAction
        )
        (pure actionResult)
    (PrimIOTry, [action]) -> do
      actionObject <- emitAtomAddress env action
      actionResult <- emitForce actionObject
      resultTy <- ioTryResultType action
      emitIOResultBranch actionResult
        ( do
            errorObject <- emitIOResultPayload actionResult
            leftObject <- emitConstructorObject eitherLeftDataConName resultTy [errorObject]
            emitMakeIOSuccess leftObject
        )
        ( do
            valueObject <- emitIOResultPayload actionResult
            rightObject <- emitConstructorObject eitherRightDataConName resultTy [valueObject]
            emitMakeIOSuccess rightObject
        )
    (PrimNewStablePtr, [value]) -> do
      valueObject <- emitAtomAddress env value
      reg <- freshRegister "stable_ptr"
      emit (ICall (Just reg) LPtr (DirectCall newStablePtrFunctionName) False [(LPtr, valueObject)])
      emitMakeIOSuccess (OLocal LPtr reg)
    (PrimDeRefStablePtr, [stable]) -> do
      stableObject <- emitAtomAddress env stable
      reg <- freshRegister "stable_value"
      emit (ICall (Just reg) LPtr (DirectCall deRefStablePtrFunctionName) False [(LPtr, stableObject)])
      emitMakeIOSuccess (OLocal LPtr reg)
    (PrimFreeStablePtr, [stable]) -> do
      stableObject <- emitAtomAddress env stable
      emit (ICall Nothing LVoid (DirectCall freeStablePtrFunctionName) False [(LPtr, stableObject)])
      unitObject <- emitConstructorObject unitDataConName unitTy []
      emitMakeIOSuccess unitObject
    (PrimCastStablePtrToPtr, [stable]) ->
      emitAtomValue env stable
    (PrimCastPtrToStablePtr, [pointer]) ->
      emitAtomValue env pointer
    (PrimFreeHaskellFunPtr, [functionPointer]) -> do
      rawFunctionPointer <- emitExpectAtomPointer env functionPointer
      emitFreeHaskellFunPtr rawFunctionPointer
      unitObject <- emitConstructorObject unitDataConName unitTy []
      emitMakeIOSuccess unitObject
    (PrimNewForeignPtr, [finalizer, pointer]) -> do
      finalizerPointer <- emitExpectAtomPointer env finalizer
      rawPointer <- emitExpectAtomPointer env pointer
      reg <- freshRegister "foreign_ptr"
      emit (ICall (Just reg) LPtr (DirectCall makeForeignPtrFunctionName) False [(LPtr, rawPointer), (LPtr, finalizerPointer)])
      emitMakeIOSuccess (OLocal LPtr reg)
    (PrimNewForeignPtr_, [pointer]) -> do
      rawPointer <- emitExpectAtomPointer env pointer
      reg <- freshRegister "foreign_ptr"
      emit (ICall (Just reg) LPtr (DirectCall makeForeignPtrFunctionName) False [(LPtr, rawPointer), (LPtr, OConstNull)])
      emitMakeIOSuccess (OLocal LPtr reg)
    (PrimAddForeignPtrFinalizer, [finalizer, foreignPtr]) -> do
      finalizerPointer <- emitExpectAtomPointer env finalizer
      foreignPtrObject <- emitAtomAddress env foreignPtr
      emit (ICall Nothing LVoid (DirectCall addForeignPtrFinalizerFunctionName) False [(LPtr, foreignPtrObject), (LPtr, finalizerPointer)])
      unitObject <- emitConstructorObject unitDataConName unitTy []
      emitMakeIOSuccess unitObject
    (PrimFinalizeForeignPtr, [foreignPtr]) -> do
      foreignPtrObject <- emitAtomAddress env foreignPtr
      emit (ICall Nothing LVoid (DirectCall finalizeForeignPtrFunctionName) False [(LPtr, foreignPtrObject)])
      unitObject <- emitConstructorObject unitDataConName unitTy []
      emitMakeIOSuccess unitObject
    (PrimWithForeignPtr, [foreignPtr, continuation]) -> do
      foreignPtrObject <- emitAtomAddress env foreignPtr
      rawPointerReg <- freshRegister "foreign_ptr_payload"
      emit (ICall (Just rawPointerReg) LPtr (DirectCall foreignPtrAddressFunctionName) False [(LPtr, foreignPtrObject)])
      pointerObject <- emitMakePointer (OLocal LPtr rawPointerReg)
      continuationObject <- emitAtomAddress env continuation
      functionObject <- emitExpectFunction continuationObject
      action <- enterFunction functionObject pointerObject
      result <- emitForce action
      emit (ICall Nothing LVoid (DirectCall touchForeignPtrFunctionName) False [(LPtr, foreignPtrObject)])
      pure result
    (PrimTouchForeignPtr, [foreignPtr]) -> do
      foreignPtrObject <- emitAtomAddress env foreignPtr
      emit (ICall Nothing LVoid (DirectCall touchForeignPtrFunctionName) False [(LPtr, foreignPtrObject)])
      unitObject <- emitConstructorObject unitDataConName unitTy []
      emitMakeIOSuccess unitObject
    (PrimUnsafeForeignPtrToPtr, [foreignPtr]) -> do
      foreignPtrObject <- emitAtomAddress env foreignPtr
      rawPointerReg <- freshRegister "foreign_ptr_payload"
      emit (ICall (Just rawPointerReg) LPtr (DirectCall foreignPtrAddressFunctionName) False [(LPtr, foreignPtrObject)])
      emitMakePointer (OLocal LPtr rawPointerReg)
    (PrimCastForeignPtr, [foreignPtr]) ->
      emitAtomValue env foreignPtr
    _ ->
      throwSTGLLVM
        ( STGLLVMUnsupported
            ( "native primitive lowering received unsupported arity for "
                <> renderCorePrimOp op
                <> ": "
                <> Text.pack (show (length arguments))
            )
        )

emitForeignCall :: ValueEnv -> CoreForeignImport -> [STGAtom] -> FunctionM LLVMOperand
emitForeignCall env foreignImport arguments =
  case S.foreignImportEntityKind (coreForeignImportEntity foreignImport) of
    S.ForeignImportDynamic ->
      emitForeignDynamicCall env foreignImport arguments
    S.ForeignImportWrapper ->
      emitForeignWrapperCall env foreignImport arguments
    _ ->
      emitStaticForeignCall env foreignImport arguments

emitStaticForeignCall :: ValueEnv -> CoreForeignImport -> [STGAtom] -> FunctionM LLVMOperand
emitStaticForeignCall env foreignImport arguments = do
  abi <- liftEitherSTGLLVM (foreignFunctionABI foreignImport)
  let (_argumentTypes, declaredResultTy) = splitForeignFunctionType (coreForeignImportType foreignImport)
      expectedArity = length (foreignFunctionArguments abi)
      actualArity = length arguments
  if expectedArity == actualArity
    then pure ()
    else
      throwSTGLLVM
        ( STGLLVMUnsupported
            ( "foreign call `"
                <> renderRName (coreForeignImportName foreignImport)
                <> "` arity mismatch during native lowering: expected "
                <> Text.pack (show expectedArity)
                <> ", got "
                <> Text.pack (show actualArity)
            )
        )
  marshalledArguments <-
    traverse
      (uncurry (emitMarshalForeignArgument env))
      (zip (foreignFunctionArguments abi) arguments)
  case foreignFunctionResult abi of
    ForeignABIVoid -> do
      emit (ICall Nothing LVoid (DirectCall (foreignFunctionSymbol abi)) False marshalledArguments)
      unitObject <- emitConstructorObject unitDataConName unitTy []
      emitForeignResult declaredResultTy unitObject
    resultABI -> do
      resultRegister <- freshRegister "foreign_result"
      emit
        ( ICall
            (Just resultRegister)
            (foreignABITypeLLVM resultABI)
            (DirectCall (foreignFunctionSymbol abi))
            False
            marshalledArguments
        )
      boxedResult <- emitBoxForeignResult resultABI (OLocal (foreignABITypeLLVM resultABI) resultRegister)
      emitForeignResult declaredResultTy boxedResult

emitForeignDynamicCall :: ValueEnv -> CoreForeignImport -> [STGAtom] -> FunctionM LLVMOperand
emitForeignDynamicCall env foreignImport arguments = do
  abi <- liftEitherSTGLLVM (foreignDynamicFunctionABI foreignImport)
  let (_argumentTypes, declaredResultTy) = splitForeignFunctionType (coreForeignImportType foreignImport)
      expectedArity = 1 + length (foreignFunctionArguments abi)
      actualArity = length arguments
  if expectedArity == actualArity
    then pure ()
    else
      throwSTGLLVM
        ( STGLLVMUnsupported
            ( "foreign dynamic call `"
                <> renderRName (coreForeignImportName foreignImport)
                <> "` arity mismatch during native lowering: expected "
                <> Text.pack (show expectedArity)
                <> ", got "
                <> Text.pack (show actualArity)
            )
        )
  case arguments of
    functionPointerAtom : callArguments -> do
      functionPointer <- emitExpectAtomPointer env functionPointerAtom
      marshalledArguments <-
        traverse
          (uncurry (emitMarshalForeignArgument env))
          (zip (foreignFunctionArguments abi) callArguments)
      case foreignFunctionResult abi of
        ForeignABIVoid -> do
          emit (ICall Nothing LVoid (IndirectCall functionPointer) False marshalledArguments)
          unitObject <- emitConstructorObject unitDataConName unitTy []
          emitForeignResult declaredResultTy unitObject
        resultABI -> do
          resultRegister <- freshRegister "foreign_dynamic_result"
          emit
            ( ICall
                (Just resultRegister)
                (foreignABITypeLLVM resultABI)
                (IndirectCall functionPointer)
                False
                marshalledArguments
            )
          boxedResult <- emitBoxForeignResult resultABI (OLocal (foreignABITypeLLVM resultABI) resultRegister)
          emitForeignResult declaredResultTy boxedResult
    [] ->
      throwSTGLLVM (STGLLVMUnsupported ("foreign dynamic call `" <> renderRName (coreForeignImportName foreignImport) <> "` is missing its FunPtr argument"))

emitForeignWrapperCall :: ValueEnv -> CoreForeignImport -> [STGAtom] -> FunctionM LLVMOperand
emitForeignWrapperCall env foreignImport arguments = do
  _ <- liftEitherSTGLLVM (foreignWrapperFunctionABI foreignImport)
  case arguments of
    [callbackAtom] -> do
      let (_argumentTypes, declaredResultTy) = splitForeignFunctionType (coreForeignImportType foreignImport)
      wrapperFunctions <- ensureForeignWrapperFunctions foreignImport
      callbackObject <- emitAtomAddress env callbackAtom
      wrapperIndex <- emitClaimForeignWrapperSlot foreignImport
      slotPointer <- emitForeignWrapperSlotPointer (foreignWrapperSlotsName foreignImport) wrapperIndex
      emit (IStore LPtr callbackObject slotPointer)
      functionPointer <- emitForeignWrapperFunctionPointer wrapperIndex wrapperFunctions
      pointerObject <- emitMakePointer functionPointer
      emitForeignResult declaredResultTy pointerObject
    _ ->
      throwSTGLLVM
        ( STGLLVMUnsupported
            ( "foreign wrapper import `"
                <> renderRName (coreForeignImportName foreignImport)
                <> "` expects exactly one Haskell callback argument during native lowering, got "
                <> Text.pack (show (length arguments))
            )
        )

ensureForeignWrapperFunctions :: CoreForeignImport -> FunctionM [Text]
ensureForeignWrapperFunctions foreignImport =
  lift $ do
    state <- get
    case Map.lookup (coreForeignImportName foreignImport) (generatedForeignWrappers state) of
      Just functionNames ->
        pure functionNames
      Nothing -> do
        abi <- liftEitherModule (foreignWrapperFunctionABI foreignImport)
        targetTy <- liftEitherModule (foreignWrapperTargetType foreignImport)
        let functionNames = foreignWrapperFunctionNames foreignImport
            slotsName = foreignWrapperSlotsName foreignImport
        functions <-
          traverse
            ( \(index, functionName) ->
                buildForeignWrapperFunction slotsName index functionName targetTy abi
            )
            (zip [0 :: Int ..] functionNames)
        modify' $
          \current ->
            current
              { generatedFunctions = generatedFunctions current <> functions
              , generatedForeignWrappers = Map.insert (coreForeignImportName foreignImport) functionNames (generatedForeignWrappers current)
              }
        pure functionNames

emitClaimForeignWrapperSlot :: CoreForeignImport -> FunctionM LLVMOperand
emitClaimForeignWrapperSlot foreignImport = do
  claimedLabel <- freshBlockLabel "wrapper_slot_claimed"
  incoming <- scan claimedLabel [] [0 .. foreignWrapperPoolSize - 1]
  startBlock claimedLabel
  indexRegister <- freshRegister "wrapper_index"
  emit (IPhi indexRegister LI64 incoming)
  pure (OLocal LI64 indexRegister)
 where
  slotsName =
    foreignWrapperSlotsName foreignImport

  scan claimedLabel incoming = \case
    [] -> do
      emitAbort
      pure incoming
    slotIndex : rest -> do
      slotPointer <- emitForeignWrapperSlotPointer slotsName (OConstInt LI64 (fromIntegral slotIndex))
      callbackRegister <- freshRegister "wrapper_slot_value"
      emit (ILoad callbackRegister LPtr slotPointer)
      freeRegister <- freshRegister "wrapper_slot_free"
      emit (IIcmp freeRegister ICmpEq LPtr (OLocal LPtr callbackRegister) OConstNull)
      freeLabel <- freshBlockLabel "wrapper_slot_free"
      nextLabel <- freshBlockLabel "wrapper_slot_next"
      terminateCurrent (TCondBr (OLocal LI1 freeRegister) freeLabel nextLabel)

      startBlock freeLabel
      freePredecessor <- getsCurrentLabel
      terminateCurrent (TBr claimedLabel)

      startBlock nextLabel
      scan claimedLabel (incoming <> [(OConstInt LI64 (fromIntegral slotIndex), freePredecessor)]) rest

emitForeignWrapperSlotPointer :: Text -> LLVMOperand -> FunctionM LLVMOperand
emitForeignWrapperSlotPointer slotsName index = do
  slotRegister <- freshRegister "wrapper_slot"
  emit
    ( IGetElementPtr
        slotRegister
        (LArray foreignWrapperPoolSize LPtr)
        (OGlobal LPtr slotsName)
        [(LI64, OConstInt LI64 0), (operandType index, index)]
    )
  pure (OLocal LPtr slotRegister)

emitForeignWrapperFunctionPointer :: LLVMOperand -> [Text] -> FunctionM LLVMOperand
emitForeignWrapperFunctionPointer index functionNames = do
  joinLabel <- freshBlockLabel "wrapper_function_join"
  incoming <- go joinLabel [] (zip [0 :: Int ..] functionNames)
  startBlock joinLabel
  resultRegister <- freshRegister "wrapper_fn"
  emit (IPhi resultRegister LPtr incoming)
  pure (OLocal LPtr resultRegister)
 where
  go joinLabel incoming = \case
    [] -> do
      emitAbort
      pure incoming
    (slotIndex, functionName) : rest -> do
      matchRegister <- freshRegister "wrapper_fn_match"
      emit (IIcmp matchRegister ICmpEq LI64 index (OConstInt LI64 (fromIntegral slotIndex)))
      matchedLabel <- freshBlockLabel "wrapper_fn_matched"
      nextLabel <- freshBlockLabel "wrapper_fn_next"
      terminateCurrent (TCondBr (OLocal LI1 matchRegister) matchedLabel nextLabel)

      startBlock matchedLabel
      matchedPredecessor <- getsCurrentLabel
      terminateCurrent (TBr joinLabel)

      startBlock nextLabel
      go joinLabel (incoming <> [(OGlobal LPtr functionName, matchedPredecessor)]) rest

buildForeignWrapperFunction :: Text -> Int -> Text -> CoreType -> ForeignFunctionABI -> ModuleM LLVMFunction
buildForeignWrapperFunction slotsName slotIndex functionName targetTy abi =
  case foreignFunctionResult abi of
    ForeignABIVoid ->
      runVoidFunction functionName params $ do
        resultObject <- emitForeignWrapperCallbackResult slotsName slotIndex abi params
        _ <- emitForeignReturnValue declaredResultTy resultObject
        pure ()
    resultABI ->
      runFunction functionName (foreignABITypeLLVM resultABI) params $ do
        resultObject <- emitForeignWrapperCallbackResult slotsName slotIndex abi params
        forcedResult <- emitForeignReturnValue declaredResultTy resultObject
        emitUnboxForeignReturn resultABI forcedResult
 where
  params =
    foreignABIParameters abi
  (_argumentTypes, declaredResultTy) =
    splitForeignFunctionType targetTy

foreignABIParameters :: ForeignFunctionABI -> [(LLVMType, Register)]
foreignABIParameters abi =
  [ (foreignABITypeLLVM argumentABI, Register ("arg" <> Text.pack (show index)))
  | (index, argumentABI) <- zip [(0 :: Int) ..] (foreignFunctionArguments abi)
  ]

emitForeignWrapperCallbackResult :: Text -> Int -> ForeignFunctionABI -> [(LLVMType, Register)] -> FunctionM LLVMOperand
emitForeignWrapperCallbackResult slotsName slotIndex abi params = do
  callbackSlot <- emitForeignWrapperSlotPointer slotsName (OConstInt LI64 (fromIntegral slotIndex))
  callbackRegister <- freshRegister "callback"
  emit (ILoad callbackRegister LPtr callbackSlot)
  freedRegister <- freshRegister "callback_freed"
  emit (IIcmp freedRegister ICmpEq LPtr (OLocal LPtr callbackRegister) OConstNull)
  freedLabel <- freshBlockLabel "callback_freed"
  liveLabel <- freshBlockLabel "callback_live"
  terminateCurrent (TCondBr (OLocal LI1 freedRegister) freedLabel liveLabel)

  startBlock freedLabel
  emitAbort

  startBlock liveLabel
  boxedArguments <-
    traverse
      ( \(argumentABI, (argumentTy, argumentRegister)) ->
          emitBoxForeignResult argumentABI (OLocal argumentTy argumentRegister)
      )
      (zip (foreignFunctionArguments abi) params)
  applyHaskellFunction (OLocal LPtr callbackRegister) boxedArguments

emitFreeHaskellFunPtr :: LLVMOperand -> FunctionM ()
emitFreeHaskellFunPtr functionPointer = do
  state <- lift get
  doneLabel <- freshBlockLabel "free_haskell_fun_ptr_done"
  scan doneLabel (wrapperCandidates (moduleWrapperImports state))
  startBlock doneLabel
 where
  wrapperCandidates imports =
    [ (foreignImport, slotIndex, functionName)
    | foreignImport <- imports
    , (slotIndex, functionName) <- zip [0 :: Int ..] (foreignWrapperFunctionNames foreignImport)
    ]

  scan doneLabel = \case
    [] ->
      emitAbort
    (foreignImport, slotIndex, functionName) : rest -> do
      matchedRegister <- freshRegister "free_haskell_fun_ptr_matched"
      emit (IIcmp matchedRegister ICmpEq LPtr functionPointer (OGlobal LPtr functionName))
      matchedLabel <- freshBlockLabel "free_haskell_fun_ptr_matched"
      nextLabel <- freshBlockLabel "free_haskell_fun_ptr_next"
      terminateCurrent (TCondBr (OLocal LI1 matchedRegister) matchedLabel nextLabel)

      startBlock matchedLabel
      slotPointer <- emitForeignWrapperSlotPointer (foreignWrapperSlotsName foreignImport) (OConstInt LI64 (fromIntegral slotIndex))
      emit (IStore LPtr OConstNull slotPointer)
      terminateCurrent (TBr doneLabel)

      startBlock nextLabel
      scan doneLabel rest

applyHaskellFunction :: LLVMOperand -> [LLVMOperand] -> FunctionM LLVMOperand
applyHaskellFunction functionObject = \case
  [] ->
    pure functionObject
  argumentObject : rest -> do
    callableObject <- emitExpectFunction functionObject
    resultObject <- enterFunction callableObject argumentObject
    applyHaskellFunction resultObject rest

emitUnboxForeignReturn :: ForeignABIType -> LLVMOperand -> FunctionM LLVMOperand
emitUnboxForeignReturn abi object =
  case abi of
    ForeignABIInteger targetTy signedness -> do
      intValue <- emitExpectObjectInt object
      emitNarrowForeignInteger targetTy signedness intValue
    ForeignABIFloating targetTy
      | targetTy == LFloat ->
          emitExpectObjectFloat object
      | targetTy == LDouble ->
          emitExpectObjectDouble object
      | otherwise ->
          throwSTGLLVM (STGLLVMUnsupported ("unsupported foreign floating ABI type " <> renderLLVMType targetTy))
    ForeignABIBool ->
      emitExpectObjectBool object
    ForeignABIChar ->
      emitExpectObjectChar object
    ForeignABIPointer ->
      emitExpectObjectPointer object
    ForeignABIVoid ->
      throwSTGLLVM (STGLLVMUnsupported "foreign callback cannot return a first-class void value")

emitMarshalForeignArgument :: ValueEnv -> ForeignABIType -> STGAtom -> FunctionM (LLVMType, LLVMOperand)
emitMarshalForeignArgument env abi atom =
  case abi of
    ForeignABIInteger targetTy signedness -> do
      value <- emitExpectAtomInt env atom
      marshalled <- emitNarrowForeignInteger targetTy signedness value
      pure (targetTy, marshalled)
    ForeignABIFloating targetTy
      | targetTy == LFloat -> do
          value <- emitExpectAtomFloat env atom
          pure (LFloat, value)
      | targetTy == LDouble -> do
          value <- emitExpectAtomDouble env atom
          pure (LDouble, value)
      | otherwise ->
          throwSTGLLVM (STGLLVMUnsupported ("unsupported foreign floating ABI type " <> renderLLVMType targetTy))
    ForeignABIBool -> do
      value <- emitExpectAtomBool env atom
      pure (LI1, value)
    ForeignABIChar -> do
      value <- emitExpectAtomChar env atom
      pure (LI32, value)
    ForeignABIPointer -> do
      value <- emitExpectAtomPointer env atom
      pure (LPtr, value)
    ForeignABIVoid ->
      throwSTGLLVM (STGLLVMUnsupported "foreign call cannot marshal a void argument")

emitNarrowForeignInteger :: LLVMType -> ForeignIntegerSignedness -> LLVMOperand -> FunctionM LLVMOperand
emitNarrowForeignInteger targetTy signedness value
  | targetTy == LI64 = do
      case signedness of
        ForeignSigned ->
          pure ()
        ForeignUnsigned ->
          emitCheckIntegerRange value (0, maxInt64Value)
      pure value
  | targetTy `elem` [LI32, LI16, LI8] = do
      range <- liftEitherSTGLLVM (foreignIntegerRange signedness targetTy)
      emitCheckIntegerRange value range
      narrowedRegister <- freshRegister "foreign_arg"
      emit (ITrunc narrowedRegister value targetTy)
      pure (OLocal targetTy narrowedRegister)
  | otherwise =
      throwSTGLLVM (STGLLVMUnsupported ("unsupported foreign integer ABI type " <> renderLLVMType targetTy))

emitBoxForeignResult :: ForeignABIType -> LLVMOperand -> FunctionM LLVMOperand
emitBoxForeignResult abi value =
  case abi of
    ForeignABIInteger sourceTy signedness -> do
      value64 <- emitWidenForeignInteger sourceTy signedness value
      emitMakeIntOperand value64
    ForeignABIFloating sourceTy
      | sourceTy == LFloat ->
          emitMakeFloat value
      | sourceTy == LDouble ->
          emitMakeDouble value
      | otherwise ->
          throwSTGLLVM (STGLLVMUnsupported ("unsupported foreign floating ABI type " <> renderLLVMType sourceTy))
    ForeignABIBool ->
      emitMakeBool value
    ForeignABIChar -> do
      value64 <- emitZExtToI64 value "foreign_char"
      emitMakeCharOperand value64
    ForeignABIPointer ->
      emitMakePointer value
    ForeignABIVoid ->
      emitConstructorObject unitDataConName unitTy []

emitForeignImportValue :: CoreForeignImport -> FunctionM LLVMOperand
emitForeignImportValue foreignImport =
  case S.foreignImportEntityKind (coreForeignImportEntity foreignImport) of
    S.ForeignImportAddress {} -> do
      symbol <- liftEitherSTGLLVM (foreignAddressSymbolName foreignImport)
      case foreignPointerKind (coreForeignImportType foreignImport) of
        Just {} ->
          emitMakePointer (OGlobal LPtr symbol)
        Nothing ->
          throwSTGLLVM (STGLLVMUnsupported ("native FFI address import `" <> renderRName (coreForeignImportName foreignImport) <> "` must have Ptr or FunPtr type"))
    other ->
      throwSTGLLVM
        ( STGLLVMUnsupported
            ( "native FFI import value lowering is not implemented for `"
                <> renderRName (coreForeignImportName foreignImport)
                <> "` with entity `"
                <> renderForeignImportEntityKind other
                <> "`"
            )
        )

emitWidenForeignInteger :: LLVMType -> ForeignIntegerSignedness -> LLVMOperand -> FunctionM LLVMOperand
emitWidenForeignInteger sourceTy signedness value
  | sourceTy == LI64 = do
      case signedness of
        ForeignSigned ->
          pure ()
        ForeignUnsigned ->
          emitCheckIntegerRange value (0, maxInt64Value)
      pure value
  | sourceTy `elem` [LI32, LI16, LI8] =
      case signedness of
        ForeignSigned -> emitSExtToI64 value "foreign_signed"
        ForeignUnsigned -> emitZExtToI64 value "foreign_unsigned"
  | otherwise =
      throwSTGLLVM (STGLLVMUnsupported ("unsupported foreign integer ABI type " <> renderLLVMType sourceTy))

emitZExtToI64 :: LLVMOperand -> Text -> FunctionM LLVMOperand
emitZExtToI64 value prefix
  | operandType value == LI64 =
      pure value
  | otherwise = do
      extendedRegister <- freshRegister prefix
      emit (IZext extendedRegister value LI64)
      pure (OLocal LI64 extendedRegister)

emitSExtToI64 :: LLVMOperand -> Text -> FunctionM LLVMOperand
emitSExtToI64 value prefix
  | operandType value == LI64 =
      pure value
  | otherwise = do
      extendedRegister <- freshRegister prefix
      emit (ISext extendedRegister value LI64)
      pure (OLocal LI64 extendedRegister)

emitCheckIntegerRange :: LLVMOperand -> (Integer, Integer) -> FunctionM ()
emitCheckIntegerRange value (minValue, maxValue)
  | operandType value /= LI64 =
      throwSTGLLVM (STGLLVMUnsupported ("foreign integer range check expected i64, got " <> renderLLVMType (operandType value)))
  | otherwise = do
      belowRegister <- freshRegister "foreign_below"
      emit (IIcmp belowRegister ICmpSlt LI64 value (OConstInt LI64 minValue))
      belowAbort <- freshBlockLabel "foreign_below_abort"
      upperCheck <- freshBlockLabel "foreign_upper_check"
      terminateCurrent (TCondBr (OLocal LI1 belowRegister) belowAbort upperCheck)

      startBlock belowAbort
      emitAbort

      startBlock upperCheck
      aboveRegister <- freshRegister "foreign_above"
      emit (IIcmp aboveRegister ICmpSlt LI64 (OConstInt LI64 maxValue) value)
      aboveAbort <- freshBlockLabel "foreign_above_abort"
      ok <- freshBlockLabel "foreign_range_ok"
      terminateCurrent (TCondBr (OLocal LI1 aboveRegister) aboveAbort ok)

      startBlock aboveAbort
      emitAbort

      startBlock ok

foreignIntegerRange :: ForeignIntegerSignedness -> LLVMType -> Either STGLLVMError (Integer, Integer)
foreignIntegerRange signedness ty =
  case (signedness, ty) of
    (ForeignSigned, LI8) -> Right (-128, 127)
    (ForeignSigned, LI16) -> Right (-32768, 32767)
    (ForeignSigned, LI32) -> Right (-2147483648, 2147483647)
    (ForeignSigned, LI64) -> Right (minInt64Value, maxInt64Value)
    (ForeignUnsigned, LI8) -> Right (0, 255)
    (ForeignUnsigned, LI16) -> Right (0, 65535)
    (ForeignUnsigned, LI32) -> Right (0, 4294967295)
    (ForeignUnsigned, LI64) -> Right (0, maxInt64Value)
    _ -> Left (STGLLVMUnsupported ("unsupported foreign integer ABI type " <> renderLLVMType ty))

