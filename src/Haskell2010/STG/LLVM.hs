data STGLLVMError
  = STGLLVMInvalidSTG [STGValidate.STGValidationError]
  | STGLLVMUnknownMain Text
  | STGLLVMAmbiguousMain Text [RName]
  | STGLLVMUnsupported Text
  | STGLLVMInvalidLLVM LLVMValidationError
  deriving stock (Show, Eq)

data ValueRef
  = DirectValue LLVMOperand
  | CapturedValue LLVMOperand Int Int
  deriving stock (Show, Eq, Ord)

type ValueEnv = Map.Map RName ValueRef

data PendingClosure = PendingClosure
  { pendingEnvPointer :: LLVMOperand
  , pendingCaptures :: [RName]
  }
  deriving stock (Show, Eq, Ord)

data AllocatedBinding = AllocatedBinding
  { allocatedName :: RName
  , allocatedObject :: LLVMOperand
  , allocatedPending :: Maybe PendingClosure
  }
  deriving stock (Show, Eq, Ord)

data ModuleState = ModuleState
  { nextClosureFunction :: Int
  , generatedFunctions :: [LLVMFunction]
  }
  deriving stock (Show, Eq)

data FunctionState = FunctionState
  { nextRegister :: Int
  , nextBlock :: Int
  , completedBlocks :: [LLVMBlock]
  , currentLabel :: Text
  , currentInstructions :: [LLVMInstruction]
  }
  deriving stock (Show, Eq)

type ModuleM = StateT ModuleState (Either STGLLVMError)
type FunctionM = StateT FunctionState ModuleM

lowerSTGProgramToLLVM :: Text -> STGProgram -> Either STGLLVMError LLVMModule
lowerSTGProgramToLLVM mainOccurrence program =
  case STGValidate.validateProgram program of
    Left errors -> Left (STGLLVMInvalidSTG errors)
    Right () -> do
      mainBinder <- findMain mainOccurrence program
      lowerValidatedSTGProgram mainBinder program

lowerSTGProgramToLLVMByName :: RName -> STGProgram -> Either STGLLVMError LLVMModule
lowerSTGProgramToLLVMByName mainName program =
  case STGValidate.validateProgram program of
    Left errors -> Left (STGLLVMInvalidSTG errors)
    Right () ->
      case [binder | bind <- stgProgramBinds program, binder <- stgBindersOf bind, stgBinderName binder == mainName] of
        [] -> Left (STGLLVMUnknownMain (nameOcc mainName))
        [mainBinder] -> lowerValidatedSTGProgram mainBinder program
        binders -> Left (STGLLVMAmbiguousMain (nameOcc mainName) (map stgBinderName binders))

lowerValidatedSTGProgram :: STGBinder -> STGProgram -> Either STGLLVMError LLVMModule
lowerValidatedSTGProgram mainBinder program = do
  (mainFunction, moduleState) <- runStateT (lowerMainFunction program mainBinder) initialModuleState
  let llvmModule =
        LLVMModule
          { moduleComments =
              [ "generated by HeggLog Haskell 2010 STG LLVM backend v0"
              , "values are boxed; thunks are forced through a lazy enter/apply runtime"
              , "heap objects use process-lifetime allocation; generated programs do not free or collect them"
              ]
          , moduleGlobals = formatGlobals
          , moduleDeclarations = runtimeDeclarations
          , moduleFunctions = runtimeFunctions <> generatedFunctions moduleState <> [mainFunction]
          }
  mapLeft STGLLVMInvalidLLVM (validateLLVMModule llvmModule)
  pure llvmModule

initialModuleState :: ModuleState
initialModuleState =
  ModuleState
    { nextClosureFunction = 0
    , generatedFunctions = []
    }

findMain :: Text -> STGProgram -> Either STGLLVMError STGBinder
findMain occurrence (STGProgram _ binds) =
  case [binder | bind <- binds, binder <- stgBindersOf bind, nameOcc (stgBinderName binder) == occurrence] of
    [] -> Left (STGLLVMUnknownMain occurrence)
    [binder] -> Right binder
    binders -> Left (STGLLVMAmbiguousMain occurrence (map stgBinderName binders))

lowerMainFunction :: STGProgram -> STGBinder -> ModuleM LLVMFunction
lowerMainFunction (STGProgram _ binds) mainBinder =
  runFunction "main" LI32 [] $ do
    env <- emitRecursiveGroup Map.empty (concatMap bindPairs binds)
    mainObject <- resolveName env (stgBinderName mainBinder)
    if stgBinderType mainBinder == ioTy unitTy
      then do
        _ <- emitForce mainObject
        pure ()
      else do
        printedValue <- printableMainValue (stgBinderType mainBinder) mainObject
        fmt <- formatPointer (stgBinderType mainBinder)
        printedReg <- freshRegister "printed"
        emit (ICall (Just printedReg) LI32 (DirectCall "printf") True [(LPtr, fmt), (operandType printedValue, printedValue)])
    pure (OConstInt LI32 0)

printableMainValue :: CoreType -> LLVMOperand -> FunctionM LLVMOperand
printableMainValue ty value
  | ty == intTy = do
      reg <- freshRegister "main_int"
      emit (ICall (Just reg) LI64 (DirectCall expectIntFunctionName) False [(LPtr, value)])
      pure (OLocal LI64 reg)
  | ty == boolTy = do
      boolReg <- freshRegister "main_bool"
      emit (ICall (Just boolReg) LI1 (DirectCall expectBoolFunctionName) False [(LPtr, value)])
      intReg <- freshRegister "main_bool_i32"
      emit (IZext intReg (OLocal LI1 boolReg) LI32)
      pure (OLocal LI32 intReg)
  | ty == charTy = do
      charReg <- freshRegister "main_char"
      emit (ICall (Just charReg) LI32 (DirectCall expectCharFunctionName) False [(LPtr, value)])
      pure (OLocal LI32 charReg)
  | otherwise =
      throwSTGLLVM (STGLLVMUnsupported ("native Haskell 2010 main must be Int, Bool, or Char, got " <> renderCoreType ty))

formatPointer :: CoreType -> FunctionM LLVMOperand
formatPointer ty = do
  reg <- freshRegister "fmt"
  let formatSpec
        | ty == boolTy = ("haskell2010_fmt_i1", boolFormatBytes)
        | ty == charTy = ("haskell2010_fmt_char", charFormatBytes)
        | otherwise = ("haskell2010_fmt_i64", intFormatBytes)
      (globalName, bytes) = formatSpec
      arrayType = LArray (Text.length bytes) LI8
  emit
    ( IGetElementPtr
        reg
        arrayType
        (OGlobal LPtr globalName)
        [(LI64, OConstInt LI64 0), (LI64, OConstInt LI64 0)]
    )
  pure (OLocal LPtr reg)

emitExpr :: ValueEnv -> STGExpr -> FunctionM LLVMOperand
emitExpr env = \case
  STGAtom atom ->
    emitAtomValue env atom
  STGApp callee arguments _ -> do
    calleeObject <- resolveName env callee
    functionObject <- emitExpectFunction calleeObject
    case arguments of
      [argument] -> do
        argumentObject <- emitAtomAddress env argument
        enterFunction functionObject argumentObject
      _ ->
        throwSTGLLVM
          ( STGLLVMUnsupported
              ("native STG enter/apply currently expects unary curried calls to `" <> renderRName callee <> "`")
          )
  STGLet bind body _ -> do
    env' <- emitBind env bind
    emitExpr env' body
  STGCase scrutinee binder alternatives _ -> do
    scrutineeObject <- emitExpr env scrutinee >>= emitForce
    emitCase env binder alternatives scrutineeObject
  STGPrim op arguments _ ->
    emitPrim env op arguments

emitBind :: ValueEnv -> STGBind -> FunctionM ValueEnv
emitBind env = \case
  STGNonRec binder rhs -> do
    allocated <- emitRhsObject env binder rhs
    let env' = Map.insert (stgBinderName binder) (DirectValue (allocatedObject allocated)) env
    fillPending env' allocated
    pure env'
  STGRec pairs ->
    emitRecursiveGroup env pairs

emitRecursiveGroup :: ValueEnv -> [(STGBinder, STGRhs)] -> FunctionM ValueEnv
emitRecursiveGroup env pairs = do
  let recCaptureEnv =
        Map.union
          (Map.fromList [(stgBinderName binder, DirectValue OConstNull) | (binder, _) <- pairs])
          env
  allocated <- traverse (uncurry (emitRhsObject recCaptureEnv)) pairs
  let recEnv =
        foldr
          (\binding -> Map.insert (allocatedName binding) (DirectValue (allocatedObject binding)))
          env
          allocated
  mapM_ (fillPending recEnv) allocated
  pure recEnv

emitRhsObject :: ValueEnv -> STGBinder -> STGRhs -> FunctionM AllocatedBinding
emitRhsObject env binder rhs =
  case rhs of
    STGFunction [argument] body -> do
      let captures = capturedNames env (freeVarsRhs rhs)
      entry <- defineFunctionEntry "fun" (stgBinderName binder) captures argument body
      envPointer <- emitAllocEnvSlots (length captures)
      object <- emitMakeFunction entry envPointer
      pure (AllocatedBinding (stgBinderName binder) object (Just (PendingClosure envPointer captures)))
    STGFunction arguments _ ->
      throwSTGLLVM
        ( STGLLVMUnsupported
            ( "native STG lowering expects unary curried functions after Core-to-STG lowering; `"
                <> renderRName (stgBinderName binder)
                <> "` has arity "
                <> Text.pack (show (length arguments))
            )
        )
    STGThunk updateFlag body -> do
      let captures = capturedNames env (freeVarsRhs rhs)
      entry <- defineThunkEntry "thunk" (stgBinderName binder) captures body
      envPointer <- emitAllocEnvSlots (length captures)
      object <- emitMakeThunk entry envPointer updateFlag
      pure (AllocatedBinding (stgBinderName binder) object (Just (PendingClosure envPointer captures)))
    STGConstructor name fields resultTy ->
      do
        fieldObjects <- traverse (emitAtomAddress env) fields
        object <- emitConstructorObject name resultTy fieldObjects
        pure (AllocatedBinding (stgBinderName binder) object Nothing)

fillPending :: ValueEnv -> AllocatedBinding -> FunctionM ()
fillPending env allocated =
  case allocatedPending allocated of
    Nothing -> pure ()
    Just pending ->
      forM_ (zip [0 :: Int ..] (pendingCaptures pending)) $ \(index, name) -> do
        value <- resolveName env name
        emitStoreEnvSlot (length (pendingCaptures pending)) (pendingEnvPointer pending) index value

defineFunctionEntry :: Text -> RName -> [RName] -> STGBinder -> STGExpr -> FunctionM Text
defineFunctionEntry prefix name captures argument body =
  lift $ do
    functionName <- freshClosureFunctionName prefix name
    function <-
      runFunction
        functionName
        LPtr
        [(LPtr, Register "env"), (LPtr, Register "arg")]
        $ do
          let envOperand = OLocal LPtr (Register "env")
              argumentOperand = OLocal LPtr (Register "arg")
              capturedEnv = captureEnv envOperand captures
              bodyEnv =
                Map.insert (stgBinderName argument) (DirectValue argumentOperand) capturedEnv
          emitExpr bodyEnv body
    addGeneratedFunction function
    pure functionName

defineThunkEntry :: Text -> RName -> [RName] -> STGExpr -> FunctionM Text
defineThunkEntry prefix name captures body =
  lift $ do
    functionName <- freshClosureFunctionName prefix name
    function <-
      runFunction functionName LPtr [(LPtr, Register "env")] $ do
        let envOperand = OLocal LPtr (Register "env")
        emitExpr (captureEnv envOperand captures) body
    addGeneratedFunction function
    pure functionName

captureEnv :: LLVMOperand -> [RName] -> ValueEnv
captureEnv envOperand captures =
  Map.fromList
    [ (name, CapturedValue envOperand index (length captures))
    | (index, name) <- zip [0 ..] captures
    ]

capturedNames :: ValueEnv -> Set.Set RName -> [RName]
capturedNames env names =
  [name | name <- Map.keys env, name `Set.member` names]

emitAtomValue :: ValueEnv -> STGAtom -> FunctionM LLVMOperand
emitAtomValue env atom =
  emitAtomAddress env atom >>= emitForce

emitAtomAddress :: ValueEnv -> STGAtom -> FunctionM LLVMOperand
emitAtomAddress env = \case
  STGVar name _ ->
    resolveName env name
  STGLit literal _ ->
    emitLiteral literal
  STGCon name ty ->
    emitConstructorObject name ty []

emitLiteral :: Literal -> FunctionM LLVMOperand
emitLiteral = \case
  LInt value ->
    case mkHIntLiteral value of
      Right intValue -> emitMakeInt (hintToInteger intValue)
      Left err -> throwSTGLLVM (STGLLVMUnsupported ("native Int literal is out of range: " <> renderIntError err))
  LChar value ->
    emitMakeChar (fromIntegral (ord value))
  LString value ->
    emitMakeStringLiteral value

emitConstructorObject :: RName -> CoreType -> [LLVMOperand] -> FunctionM LLVMOperand
emitConstructorObject name ty fields
  | ty == boolTy && name == trueDataConName = emitMakeBool (OConstInt LI1 1)
  | ty == boolTy && name == falseDataConName = emitMakeBool (OConstInt LI1 0)
  | otherwise =
      emitMakeData name fields

emitPrim :: ValueEnv -> CorePrimOp -> [STGAtom] -> FunctionM LLVMOperand
emitPrim env op arguments =
  case (op, arguments) of
    (PrimAdd, [lhs, rhs]) -> emitIntBinary env "llvm.sadd.with.overflow.i64" lhs rhs >>= emitMakeIntOperand
    (PrimSub, [lhs, rhs]) -> emitIntBinary env "llvm.ssub.with.overflow.i64" lhs rhs >>= emitMakeIntOperand
    (PrimMul, [lhs, rhs]) -> emitIntBinary env "llvm.smul.with.overflow.i64" lhs rhs >>= emitMakeIntOperand
    (PrimDiv, [lhs, rhs]) -> emitCheckedDiv env lhs rhs >>= emitMakeIntOperand
    (PrimLt, [lhs, rhs]) -> do
      lhsValue <- emitExpectAtomInt env lhs
      rhsValue <- emitExpectAtomInt env rhs
      reg <- freshRegister "lt"
      emit (IIcmp reg ICmpSlt LI64 lhsValue rhsValue)
      emitMakeBool (OLocal LI1 reg)
    (PrimEq, [lhs, rhs])
      | stgAtomType lhs == intTy -> do
          lhsValue <- emitExpectAtomInt env lhs
          rhsValue <- emitExpectAtomInt env rhs
          reg <- freshRegister "eq"
          emit (IIcmp reg ICmpEq LI64 lhsValue rhsValue)
          emitMakeBool (OLocal LI1 reg)
      | stgAtomType lhs == boolTy -> do
          lhsValue <- emitExpectAtomBool env lhs
          rhsValue <- emitExpectAtomBool env rhs
          reg <- freshRegister "eq"
          emit (IIcmp reg ICmpEq LI1 lhsValue rhsValue)
          emitMakeBool (OLocal LI1 reg)
      | stgAtomType lhs == charTy -> do
          lhsValue <- emitExpectAtomChar env lhs
          rhsValue <- emitExpectAtomChar env rhs
          reg <- freshRegister "eq"
          emit (IIcmp reg ICmpEq LI32 lhsValue rhsValue)
          emitMakeBool (OLocal LI1 reg)
      | otherwise ->
          throwSTGLLVM
            ( STGLLVMUnsupported
                ("native equality is implemented for Int, Bool, and Char, got " <> renderCoreType (stgAtomType lhs))
            )
    (PrimNegate, [value]) -> do
      valueOperand <- emitExpectAtomInt env value
      emitCheckedSub (OConstInt LI64 0) valueOperand >>= emitMakeIntOperand
    (PrimCharToInt, [value]) -> do
      valueOperand <- emitExpectAtomChar env value
      extendedReg <- freshRegister "char_i64"
      emit (IZext extendedReg valueOperand LI64)
      emitMakeIntOperand (OLocal LI64 extendedReg)
    (PrimIntToChar, [value]) -> do
      valueOperand <- emitExpectAtomInt env value
      emitCheckCharCode valueOperand
      emitMakeCharOperand valueOperand
    (PrimShowInt, [value]) -> do
      valueOperand <- emitExpectAtomInt env value
      resultReg <- freshRegister "show_int"
      emit (ICall (Just resultReg) LPtr (DirectCall showIntFunctionName) False [(LI64, valueOperand)])
      pure (OLocal LPtr resultReg)
    (PrimShowBool, [value]) -> do
      valueOperand <- emitExpectAtomBool env value
      emitShowBool valueOperand
    (PrimPutStrLn, [value]) -> do
      valueObject <- emitAtomAddress env value
      emit (ICall Nothing LVoid (DirectCall putStrLnFunctionName) False [(LPtr, valueObject)])
      emitConstructorObject unitDataConName unitTy []
    (PrimIOThen, [firstAction, secondAction]) -> do
      firstObject <- emitAtomAddress env firstAction
      _ <- emitForce firstObject
      secondObject <- emitAtomAddress env secondAction
      emitForce secondObject
    (PrimIOBind, [firstAction, continuation]) -> do
      firstObject <- emitAtomAddress env firstAction
      firstResult <- emitForce firstObject
      continuationObject <- emitAtomAddress env continuation
      functionObject <- emitExpectFunction continuationObject
      secondAction <- enterFunction functionObject firstResult
      emitForce secondAction
    (PrimIOReturn, [value]) ->
      emitAtomAddress env value
    _ ->
      throwSTGLLVM
        ( STGLLVMUnsupported
            ( "native primitive lowering received unsupported arity for "
                <> renderCorePrimOp op
                <> ": "
                <> Text.pack (show (length arguments))
            )
        )

emitIntBinary :: ValueEnv -> Text -> STGAtom -> STGAtom -> FunctionM LLVMOperand
emitIntBinary env intrinsic lhs rhs = do
  lhsValue <- emitExpectAtomInt env lhs
  rhsValue <- emitExpectAtomInt env rhs
  emitCheckedIntPrim intrinsic lhsValue rhsValue

emitExpectAtomInt :: ValueEnv -> STGAtom -> FunctionM LLVMOperand
emitExpectAtomInt env atom = do
  object <- emitAtomAddress env atom
  reg <- freshRegister "int"
  emit (ICall (Just reg) LI64 (DirectCall expectIntFunctionName) False [(LPtr, object)])
  pure (OLocal LI64 reg)

emitExpectAtomBool :: ValueEnv -> STGAtom -> FunctionM LLVMOperand
emitExpectAtomBool env atom = do
  object <- emitAtomAddress env atom
  reg <- freshRegister "bool"
  emit (ICall (Just reg) LI1 (DirectCall expectBoolFunctionName) False [(LPtr, object)])
  pure (OLocal LI1 reg)

emitExpectAtomChar :: ValueEnv -> STGAtom -> FunctionM LLVMOperand
emitExpectAtomChar env atom = do
  object <- emitAtomAddress env atom
  reg <- freshRegister "char"
  emit (ICall (Just reg) LI32 (DirectCall expectCharFunctionName) False [(LPtr, object)])
  pure (OLocal LI32 reg)

emitShowBool :: LLVMOperand -> FunctionM LLVMOperand
emitShowBool value = do
  trueLabel <- freshBlockLabel "show_bool_true"
  falseLabel <- freshBlockLabel "show_bool_false"
  resultLabel <- freshBlockLabel "show_bool_join"
  terminateCurrent (TCondBr value trueLabel falseLabel)

  startBlock trueLabel
  trueObject <- emitMakeStringLiteral "True"
  truePredecessor <- getsCurrentLabel
  terminateCurrent (TBr resultLabel)

  startBlock falseLabel
  falseObject <- emitMakeStringLiteral "False"
  falsePredecessor <- getsCurrentLabel
  terminateCurrent (TBr resultLabel)

  startBlock resultLabel
  resultReg <- freshRegister "show_bool"
  emit (IPhi resultReg LPtr [(trueObject, truePredecessor), (falseObject, falsePredecessor)])
  pure (OLocal LPtr resultReg)

emitCheckedIntPrim :: Text -> LLVMOperand -> LLVMOperand -> FunctionM LLVMOperand
emitCheckedIntPrim intrinsic lhs rhs = do
  pairReg <- freshRegister "checked"
  let pairType = LStruct [LI64, LI1]
  emit (ICall (Just pairReg) pairType (DirectCall intrinsic) False [(LI64, lhs), (LI64, rhs)])
  let pairOperand = OLocal pairType pairReg
  valueReg <- freshRegister "value"
  overflowReg <- freshRegister "overflow"
  emit (IExtractValue valueReg LI64 pairOperand 0)
  emit (IExtractValue overflowReg LI1 pairOperand 1)
  abortLabel <- freshBlockLabel "int_overflow"
  continueLabel <- freshBlockLabel "int_ok"
  terminateCurrent (TCondBr (OLocal LI1 overflowReg) abortLabel continueLabel)

  startBlock abortLabel
  emitAbort

  startBlock continueLabel
  pure (OLocal LI64 valueReg)

emitCheckedSub :: LLVMOperand -> LLVMOperand -> FunctionM LLVMOperand
emitCheckedSub =
  emitCheckedIntPrim "llvm.ssub.with.overflow.i64"

emitCheckCharCode :: LLVMOperand -> FunctionM ()
emitCheckCharCode value = do
  belowZeroReg <- freshRegister "char_below_zero"
  emit (IIcmp belowZeroReg ICmpSlt LI64 value (OConstInt LI64 0))
  belowZeroAbortLabel <- freshBlockLabel "char_below_zero_abort"
  upperCheckLabel <- freshBlockLabel "char_upper_check"
  terminateCurrent (TCondBr (OLocal LI1 belowZeroReg) belowZeroAbortLabel upperCheckLabel)

  startBlock belowZeroAbortLabel
  emitAbort

  startBlock upperCheckLabel
  aboveUnicodeReg <- freshRegister "char_above_unicode"
  emit (IIcmp aboveUnicodeReg ICmpSlt LI64 (OConstInt LI64 0x10FFFF) value)
  aboveUnicodeAbortLabel <- freshBlockLabel "char_above_unicode_abort"
  okLabel <- freshBlockLabel "char_ok"
  terminateCurrent (TCondBr (OLocal LI1 aboveUnicodeReg) aboveUnicodeAbortLabel okLabel)

  startBlock aboveUnicodeAbortLabel
  emitAbort

  startBlock okLabel

emitCheckedDiv :: ValueEnv -> STGAtom -> STGAtom -> FunctionM LLVMOperand
emitCheckedDiv env lhs rhs = do
  lhsValue <- emitExpectAtomInt env lhs
  rhsValue <- emitExpectAtomInt env rhs
  zeroReg <- freshRegister "div_zero"
  emit (IIcmp zeroReg ICmpEq LI64 rhsValue (OConstInt LI64 0))
  zeroLabel <- freshBlockLabel "div_zero_abort"
  nonZeroLabel <- freshBlockLabel "div_nonzero"
  terminateCurrent (TCondBr (OLocal LI1 zeroReg) zeroLabel nonZeroLabel)

  startBlock zeroLabel
  emitAbort

  startBlock nonZeroLabel
  minReg <- freshRegister "div_min_lhs"
  emit (IIcmp minReg ICmpEq LI64 lhsValue (OConstInt LI64 minInt64Value))
  overflowCheckLabel <- freshBlockLabel "div_overflow_check"
  okLabel <- freshBlockLabel "div_ok"
  terminateCurrent (TCondBr (OLocal LI1 minReg) overflowCheckLabel okLabel)

  startBlock overflowCheckLabel
  negOneReg <- freshRegister "div_neg_one"
  emit (IIcmp negOneReg ICmpEq LI64 rhsValue (OConstInt LI64 (-1)))
  overflowLabel <- freshBlockLabel "div_overflow_abort"
  terminateCurrent (TCondBr (OLocal LI1 negOneReg) overflowLabel okLabel)

  startBlock overflowLabel
  emitAbort

  startBlock okLabel
  valueReg <- freshRegister "div"
  emit (IDiv valueReg LI64 lhsValue rhsValue)
  pure (OLocal LI64 valueReg)

emitCase :: ValueEnv -> STGBinder -> [STGAlt] -> LLVMOperand -> FunctionM LLVMOperand
emitCase env binder alternatives scrutineeObject = do
  tagValue <- loadObjectI64Field scrutineeObject 1 "case_payload"
  resultLabel <- freshBlockLabel "case_join"
  let caseEnv = Map.insert (stgBinderName binder) (DirectValue scrutineeObject) env
  incoming <- emitCaseDecision caseEnv scrutineeObject tagValue alternatives resultLabel []
  startBlock resultLabel
  case incoming of
    [] ->
      pure OConstNull
    _ -> do
      resultReg <- freshRegister "case_result"
      emit (IPhi resultReg LPtr incoming)
      pure (OLocal LPtr resultReg)

emitCaseDecision ::
  ValueEnv ->
  LLVMOperand ->
  LLVMOperand ->
  [STGAlt] ->
  Text ->
  [(LLVMOperand, Text)] ->
  FunctionM [(LLVMOperand, Text)]
emitCaseDecision env scrutineeObject tagValue alternatives resultLabel incoming =
  case alternatives of
    [] -> do
      emitAbort
      pure incoming
    STGAlt altCon binders body : rest -> do
      case altCon of
        DefaultAlt ->
          emitCaseBody env body resultLabel incoming
        LiteralAlt literal -> do
          matchedLabel <- freshBlockLabel "case_lit"
          nextLabel <- freshBlockLabel "case_next"
          condition <- literalMatchCondition tagValue literal
          terminateCurrent (TCondBr condition matchedLabel nextLabel)
          startBlock matchedLabel
          incoming' <- emitCaseBody env body resultLabel incoming
          startBlock nextLabel
          emitCaseDecision env scrutineeObject tagValue rest resultLabel incoming'
        ConstructorAlt name
          | name == trueDataConName || name == falseDataConName -> do
              matchedLabel <- freshBlockLabel "case_bool"
              nextLabel <- freshBlockLabel "case_next"
              let expected = if name == trueDataConName then 1 else 0
              reg <- freshRegister "case_bool_match"
              emit (IIcmp reg ICmpEq LI64 tagValue (OConstInt LI64 expected))
              terminateCurrent (TCondBr (OLocal LI1 reg) matchedLabel nextLabel)
              startBlock matchedLabel
              incoming' <- emitCaseBody env body resultLabel incoming
              startBlock nextLabel
              emitCaseDecision env scrutineeObject tagValue rest resultLabel incoming'
          | otherwise ->
              emitConstructorCaseBranch env scrutineeObject tagValue name binders body rest resultLabel incoming

emitConstructorCaseBranch ::
  ValueEnv ->
  LLVMOperand ->
  LLVMOperand ->
  RName ->
  [STGBinder] ->
  STGExpr ->
  [STGAlt] ->
  Text ->
  [(LLVMOperand, Text)] ->
  FunctionM [(LLVMOperand, Text)]
emitConstructorCaseBranch env scrutineeObject tagValue name binders body rest resultLabel incoming = do
  matchedLabel <- freshBlockLabel "case_data"
  nextLabel <- freshBlockLabel "case_next"
  reg <- freshRegister "case_data_match"
  emit (IIcmp reg ICmpEq LI64 tagValue (OConstInt LI64 (constructorRuntimeTag name)))
  terminateCurrent (TCondBr (OLocal LI1 reg) matchedLabel nextLabel)
  startBlock matchedLabel
  fieldEnv <- emitConstructorFieldEnv scrutineeObject env binders
  incoming' <- emitCaseBody fieldEnv body resultLabel incoming
  startBlock nextLabel
  emitCaseDecision env scrutineeObject tagValue rest resultLabel incoming'

emitConstructorFieldEnv :: LLVMOperand -> ValueEnv -> [STGBinder] -> FunctionM ValueEnv
emitConstructorFieldEnv scrutineeObject env binders = do
  fieldsPointer <- loadObjectPtrField scrutineeObject 2 "data_fields"
  arity <- loadObjectI64Field scrutineeObject 5 "data_arity"
  arityOk <- freshRegister "data_arity_ok"
  emit (IIcmp arityOk ICmpEq LI64 arity (OConstInt LI64 (fromIntegral (length binders))))
  abortLabel <- freshBlockLabel "data_arity_abort"
  continueLabel <- freshBlockLabel "data_arity_ok"
  terminateCurrent (TCondBr (OLocal LI1 arityOk) continueLabel abortLabel)

  startBlock abortLabel
  emitAbort

  startBlock continueLabel
  fieldBindings <- traverse (loadField fieldsPointer) (zip [0 :: Int ..] binders)
  pure (Map.union (Map.fromList fieldBindings) env)
 where
  loadField fieldsPointer (index, binder) = do
    field <- envSlotPointer (length binders) fieldsPointer index
    reg <- freshRegister "data_field"
    emit (ILoad reg LPtr field)
    pure (stgBinderName binder, DirectValue (OLocal LPtr reg))

emitCaseBody :: ValueEnv -> STGExpr -> Text -> [(LLVMOperand, Text)] -> FunctionM [(LLVMOperand, Text)]
emitCaseBody env body resultLabel incoming = do
  value <- emitExpr env body
  predecessor <- getsCurrentLabel
  terminateCurrent (TBr resultLabel)
  pure (incoming <> [(value, predecessor)])

literalMatchCondition :: LLVMOperand -> Literal -> FunctionM LLVMOperand
literalMatchCondition tagValue = \case
  LInt value ->
    case mkHIntLiteral value of
      Right intValue -> do
        reg <- freshRegister "case_int_match"
        emit (IIcmp reg ICmpEq LI64 tagValue (OConstInt LI64 (hintToInteger intValue)))
        pure (OLocal LI1 reg)
      Left err ->
        throwSTGLLVM (STGLLVMUnsupported ("native Int case literal is out of range: " <> renderIntError err))
  LChar expected -> do
    reg <- freshRegister "case_char_match"
    emit (IIcmp reg ICmpEq LI64 tagValue (OConstInt LI64 (fromIntegral (ord expected))))
    pure (OLocal LI1 reg)
  LString {} ->
    throwSTGLLVM (STGLLVMUnsupported "native String case alternatives are not implemented for Core-0")

resolveName :: ValueEnv -> RName -> FunctionM LLVMOperand
resolveName env name =
  case Map.lookup name env of
    Nothing ->
      throwSTGLLVM (STGLLVMUnsupported ("native STG lowering found unbound variable `" <> renderRName name <> "`"))
    Just (DirectValue operand) ->
      pure operand
    Just (CapturedValue envPointer index count) ->
      emitLoadEnvSlot count envPointer index

