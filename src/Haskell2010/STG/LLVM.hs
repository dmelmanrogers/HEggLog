  LLVMFunction
    { functionName = allocObjectFunctionName
    , functionReturnType = LPtr
    , functionParams = []
    , functionBlocks =
        [ LLVMBlock
            "entry"
            [ ICall (Just (Register "obj")) LPtr (DirectCall "malloc") False [(LI64, OConstInt LI64 objectSize)]
            , IIcmp (Register "is_null") ICmpEq LPtr (OLocal LPtr (Register "obj")) OConstNull
            ]
            (TCondBr (OLocal LI1 (Register "is_null")) "abort" "ok")
        , LLVMBlock
            "abort"
            [ICall Nothing LVoid (DirectCall "abort") False []]
            TUnreachable
        , LLVMBlock
            "ok"
            []
            (TRet LPtr (OLocal LPtr (Register "obj")))
        ]
    }
