  "declare "
    <> renderLLVMType (foreignABITypeLLVM (foreignFunctionResult abi))
    <> " @"
    <> foreignFunctionSymbol abi
    <> "("
    <> Text.intercalate ", " (map (renderLLVMType . foreignABITypeLLVM) (foreignFunctionArguments abi))
    <> ")"

