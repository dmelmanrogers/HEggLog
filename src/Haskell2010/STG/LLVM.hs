runFunction :: Text -> LLVMType -> [(LLVMType, Register)] -> FunctionM LLVMOperand -> ModuleM LLVMFunction
runFunction name returnType params action = do
  (_, finalState) <-
    runStateT
      ( do
          value <- action
          terminateCurrent (TRet returnType value)
      )
      initialFunctionState
  pure
    LLVMFunction
      { functionName = name
      , functionReturnType = returnType
      , functionParams = params
      , functionBlocks = completedBlocks finalState
      }

initialFunctionState :: FunctionState
initialFunctionState =
  FunctionState
    { nextRegister = 0
    , nextBlock = 0
    , completedBlocks = []
    , currentLabel = "entry"
    , currentInstructions = []
    }

freshRegister :: Text -> FunctionM Register
freshRegister prefix = do
  state <- get
  let reg = Register (prefix <> Text.pack (show (nextRegister state)))
  modify' (\st -> st {nextRegister = nextRegister st + 1})
  pure reg

freshBlockLabel :: Text -> FunctionM Text
freshBlockLabel prefix = do
  state <- get
  let label = "bb_" <> prefix <> Text.pack (show (nextBlock state))
  modify' (\st -> st {nextBlock = nextBlock st + 1})
  pure label

getsCurrentLabel :: FunctionM Text
