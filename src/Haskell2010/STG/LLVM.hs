freeVarsRhs :: STGRhs -> Set.Set RName
freeVarsRhs = \case
  STGFunction binders body ->
    freeVarsExpr body `Set.difference` Set.fromList (map stgBinderName binders)
  STGThunk _ body ->
    freeVarsExpr body
  STGConstructor _ fields _ ->
    Set.unions (map freeVarsAtom fields)

freeVarsExpr :: STGExpr -> Set.Set RName
freeVarsExpr = \case
  STGAtom atom ->
    freeVarsAtom atom
  STGApp callee arguments _ ->
    Set.insert callee (Set.unions (map freeVarsAtom arguments))
  STGLet bind body _ ->
    freeVarsBind bind <> (freeVarsExpr body `Set.difference` Set.fromList (map stgBinderName (stgBindersOf bind)))
  STGCase scrutinee binder alternatives _ ->
    freeVarsExpr scrutinee
      <> Set.unions
        [ freeVarsExpr body `Set.difference` Set.fromList (stgBinderName binder : map stgBinderName binders)
        | STGAlt _ binders body <- alternatives
        ]
  STGPrim _ arguments _ ->
    Set.unions (map freeVarsAtom arguments)

freeVarsBind :: STGBind -> Set.Set RName
freeVarsBind = \case
  STGNonRec _ rhs ->
    freeVarsRhs rhs
  STGRec pairs ->
    Set.unions (map (freeVarsRhs . snd) pairs)
      `Set.difference` Set.fromList [stgBinderName binder | (binder, _) <- pairs]

freeVarsAtom :: STGAtom -> Set.Set RName
freeVarsAtom = \case
  STGVar name _ -> Set.singleton name
  STGLit {} -> Set.empty
  STGCon {} -> Set.empty

bindPairs :: STGBind -> [(STGBinder, STGRhs)]
bindPairs = \case
  STGNonRec binder rhs -> [(binder, rhs)]
  STGRec pairs -> pairs

