sanitizeName :: Text -> Text
sanitizeName =
  Text.concatMap encode
 where
  encode c
    | c >= 'a' && c <= 'z' = Text.singleton c
    | c >= 'A' && c <= 'Z' = Text.singleton c
    | c >= '0' && c <= '9' = Text.singleton c
    | c == '_' = "_u"
    | otherwise = "_x" <> Text.pack (showHex (ord c) "") <> "_"

