renderSTGLLVMError :: STGLLVMError -> Text
renderSTGLLVMError = \case
  STGLLVMInvalidSTG errors ->
    "invalid STG before native lowering: "
      <> Text.intercalate "; " (map STGValidate.renderSTGValidationError errors)
  STGLLVMUnknownMain occurrence ->
    "native Haskell 2010 output requires a `"
      <> occurrence
      <> "` binding"
  STGLLVMAmbiguousMain occurrence names ->
    "native Haskell 2010 output found multiple `"
      <> occurrence
      <> "` bindings: "
      <> Text.intercalate ", " (map renderRName names)
  STGLLVMUnsupported message ->
    "unsupported native Haskell 2010 STG form: " <> message
  STGLLVMInvalidLLVM err ->
    "invalid LLVM generated from Haskell 2010 STG: " <> renderLLVMValidationError err
