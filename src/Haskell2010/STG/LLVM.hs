  failurePredecessor <- getsCurrentLabel
  terminateCurrent (TBr joinLabel)

  startBlock joinLabel
  result <- freshRegister "exitcode_result"
  emit
    ( IPhi
        result
        LPtr
        [ (successResult, successPredecessor)
        , (failureZeroResult, failureZeroPredecessor)
